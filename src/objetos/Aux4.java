package objetos;

import java.util.Set;

public class Aux4 {

	String prop;
	Set<String> setValores;

	public Aux4() {

	}

	public Aux4(String prop, Set<String> setValores) {
		this.prop = prop;
		this.setValores = setValores;
	}

	public String getProp() {
		return prop;
	}

	public void setProp(String prop) {
		this.prop = prop;
	}

	public Set<String> getSetValores() {
		return setValores;
	}

	public void setSetValores(Set<String> setValores) {
		this.setValores = setValores;
	}

}
