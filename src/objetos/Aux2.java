package objetos;

public class Aux2 {

	String ramoAtual;
	String nohAtual;
	String valor;
	
	public Aux2() {
	}
	
	public Aux2(String ramoAtual, String nohAtual) {
		this.ramoAtual = ramoAtual;
		this.nohAtual = nohAtual;
	}
		
	public String getRamoAtual() {
		return ramoAtual;
	}
	public void setRamoAtual(String ramoAtual) {
		this.ramoAtual = ramoAtual;
	}
	public String getNohAtual() {
		return nohAtual;
	}
	public void setNohAtual(String nohAtual) {
		this.nohAtual = nohAtual;
	}
	public void setValor(String ramoAtual) {
		this.ramoAtual = ramoAtual;
	}
	public String getValor() {
		return nohAtual;
	}

}
